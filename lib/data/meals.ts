export function getMealsFor(user: ProfileId, day: DayKey) {
  return MEALS.filter((m) => m.user === user && m.day === day).sort(
    (a, b) => a.slot - b.slot
  );
}

export function getDayMacros(user: ProfileId, day: DayKey) {
  const meals = getMealsFor(user, day);
  return {
    kcal: meals.reduce((sum, m) => sum + m.kcal, 0),
    proteinG: meals.reduce((sum, m) => sum + m.proteinG, 0),
    mealCount: meals.length,
  };
}
